#operasi logika bolean

#konjunsi
print ("\n""====== konjungsi =====""\n")
print ( "false and false =", False and False) #hasil false
print ( "false and true =", False and True) #hasil false
print ( "true and false =", True and False) #hasil false
print ( "true and true =", True and True) #hasil true
#dinyatakan benar apabila dua pernyataan benar

#disjungsi
print ("\n""====== Disjungsi ======""\n")
print ( "false or false =", False or False) #hasil false
print ( "false or true =", False or True) #hasil true
print ( "true or false =", True or False) #hasil true
print ( "true or true =", True or True) #hasil true
#dinyatakan benar apabila salah satu pernyataan benar

#XOR (EXCLUSIVE OR)
print ("\n""===== XOR (EXCLUSIVE OR) =====""\n")
print ( "false XOR false =",False ^ False) #hasil false
print ( "false XOR true =", False ^ True) #hasil true
print ( "true XOR false =", True ^ False) #hasil true
print ( "true XOR true =", True ^ True) #hasil false
#dinyatakan benar apabila hanya satu pernyataan benar

