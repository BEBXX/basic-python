#penulisan string bisa memakai '' atau "" dan bisa digabung sesuai kondisi
print ("ini adalah string")
print ('ini juga string')
print ('"hai ini adalah string"')
print ("Hari ini hari jum'at")

#memberikan jarak pada kata menggunakan \t atau \b
print ("aku \tkamu") #jauh (sama kayak fungsi tab utk memberikan jarak)
print ("aku \bkamu") #deketan ( sama kayak backspace)

#penggunaan back slash
print ("C:\\user\\albert")
print ('hari ini hari jum\'at')

#new line menggunakan \n atau \r\n
print ("ini baris pertama \nini baris kedua")
print ("ini baris pertama \r\nini baris kedua")

#raw string biar gausah make double back slash jadi lebih mudah
print (r"C:\user\ivan")

#multi line spacing 
print ("""

ini baris pertama 
dan ini baris kedua 

""")